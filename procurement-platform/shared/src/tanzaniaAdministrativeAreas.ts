export function getTanzaniaRegions(): string[] {
  return tanzaniaAdministrativeAreas.regions.map((region) => region.name);
}

export function getDistrictsForRegion(regionName: string): string[] {
  const region = tanzaniaAdministrativeAreas.regions.find((item) => item.name === regionName);
  return region ? region.districts.map((district) => district.name) : [];
}

export function getWardsForDistrict(regionName: string, districtName: string): string[] {
  const region = tanzaniaAdministrativeAreas.regions.find((item) => item.name === regionName);
  const district = region?.districts.find((item) => item.name === districtName);
  return district ? [...district.wards] : [];
}

export function isCompleteTanzaniaLocation(value: unknown): value is TanzaniaLocationSelection {
  if (!value || typeof value !== 'object') return false;
  const candidate = value as Partial<TanzaniaLocationSelection>;
  return Boolean(candidate.region?.trim() && candidate.district?.trim() && candidate.ward?.trim());
}

export function isValidTanzaniaLocation(value: unknown): value is TanzaniaLocationSelection {
  if (!isCompleteTanzaniaLocation(value)) return false;
  return getWardsForDistrict(value.region, value.district).includes(value.ward);
}
